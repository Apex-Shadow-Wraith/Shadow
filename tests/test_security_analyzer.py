class TestStoreSecurityKnowledge:
    """Tests for Grimoire storage."""

    def test_store_calls_grimoire(self, analyzer_with_grimoire: SecurityAnalyzer) -> None:
        """Store calls Grimoire.remember with correct params."""
        knowledge = {
            "topic": "test_topic",
            "explanation": "Test explanation",
            "how_it_works": "Test mechanism",
            "when_to_use": "Test scenario",
            "shadow_relevance": "Test relevance",
        }
        count = analyzer_with_grimoire.store_security_knowledge(knowledge, "test")

        assert count == 1
        analyzer_with_grimoire._grimoire.remember.assert_called_once()
        call_kwargs = analyzer_with_grimoire._grimoire.remember.call_args
        assert call_kwargs[1]["source_module"] == "sentinel"
        assert call_kwargs[1]["category"] == "security_knowledge"
        assert "firewall" in call_kwargs[1]["tags"]

    def test_store_deduplicates(self, analyzer_with_grimoire: SecurityAnalyzer) -> None:
        """Store passes check_duplicates=True to Grimoire."""
        knowledge = {"topic": "dup_test", "explanation": "Test"}
        analyzer_with_grimoire.store_security_knowledge(knowledge, "test")

        call_kwargs = analyzer_with_grimoire._grimoire.remember.call_args
        assert call_kwargs[1]["check_duplicates"] is True

    def test_store_without_grimoire(self, analyzer: SecurityAnalyzer) -> None:
        """Store without Grimoire returns 0."""
        count = analyzer.store_security_knowledge({"topic": "test"}, "test")
        assert count == 0

    def test_store_handles_grimoire_error(self, analyzer_with_grimoire: SecurityAnalyzer) -> None:
        """Store handles Grimoire errors gracefully."""
        analyzer_with_grimoire._grimoire.remember.side_effect = RuntimeError("DB error")
        count = analyzer_with_grimoire.store_security_knowledge(
            {"topic": "error_test", "explanation": "test"}, "test",
        )
        assert count == 0
