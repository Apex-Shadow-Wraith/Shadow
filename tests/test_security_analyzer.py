class TestEvaluateFirewall:
    """Tests for firewall evaluation/scoring."""

    def test_default_deny_passes(self, analyzer: SecurityAnalyzer) -> None:
        """Default deny scores as pass."""
        analysis = analyzer.analyze_firewall_config(IPTABLES_CONFIG, "iptables")
        evaluation = analyzer.evaluate_firewall(analysis)

        # INPUT is DROP, FORWARD is DROP — at least partial
        dd = evaluation["checks"]["default_deny"]
        assert dd["status"] in ("pass", "partial")
        assert dd["score"] >= 2

    def test_default_accept_fails(self, analyzer: SecurityAnalyzer) -> None:
        """Default accept scores as fail."""
        config = """\
Chain INPUT (policy ACCEPT)
target     prot opt source               destination
ACCEPT     tcp  --  anywhere             anywhere             tcp dpt:ssh

Chain FORWARD (policy ACCEPT)
target     prot opt source               destination

Chain OUTPUT (policy ACCEPT)
target     prot opt source               destination
"""
        analysis = analyzer.analyze_firewall_config(config, "iptables")
        evaluation = analyzer.evaluate_firewall(analysis)

        assert evaluation["checks"]["default_deny"]["status"] == "fail"
        assert evaluation["checks"]["default_deny"]["score"] == 1

    def test_missing_egress_filtering(self, analyzer: SecurityAnalyzer) -> None:
        """Missing egress filtering is flagged."""
        config = """\
Chain INPUT (policy DROP)
target     prot opt source               destination
ACCEPT     tcp  --  anywhere             anywhere             tcp dpt:ssh

Chain FORWARD (policy DROP)
target     prot opt source               destination

Chain OUTPUT (policy ACCEPT)
target     prot opt source               destination
"""
        analysis = analyzer.analyze_firewall_config(config, "iptables")
        evaluation = analyzer.evaluate_firewall(analysis)

        egress = evaluation["checks"]["egress_filtering"]
        assert egress["status"] == "fail"
        assert egress["score"] == 1

    def test_good_config_gets_high_grade(self, analyzer: SecurityAnalyzer) -> None:
        """Well-configured firewall gets A or B grade."""
        analysis = analyzer.analyze_firewall_config(NFTABLES_CONFIG, "nftables")
        evaluation = analyzer.evaluate_firewall(analysis)

        assert evaluation["grade"] in ("A", "B")
        assert evaluation["total_score"] >= 18

    def test_evaluation_returns_recommendations(self, analyzer: SecurityAnalyzer) -> None:
        """Evaluation includes actionable recommendations."""
        config = """\
Chain INPUT (policy ACCEPT)
target     prot opt source               destination
ACCEPT     tcp  --  anywhere             anywhere             tcp dpt:ssh
ACCEPT     tcp  --  anywhere             anywhere             tcp dpt:http

Chain OUTPUT (policy ACCEPT)
target     prot opt source               destination
"""
        analysis = analyzer.analyze_firewall_config(config, "iptables")
        evaluation = analyzer.evaluate_firewall(analysis)

        assert len(evaluation["recommendations"]) > 0
        assert evaluation["grade"] in ("D", "F")

    def test_error_analysis_returns_error(self, analyzer: SecurityAnalyzer) -> None:
        """Evaluation of error analysis returns error."""
        result = analyzer.evaluate_firewall({"error": "bad config"})
        assert result["error"] == "bad config"


