class TestAnalyzeFirewallConfig:
    """Tests for parsing firewall configs."""

    def test_parse_iptables_rules(self, analyzer: SecurityAnalyzer) -> None:
        """iptables -L output is parsed correctly."""
        result = analyzer.analyze_firewall_config(IPTABLES_CONFIG, "iptables")

        assert result["firewall_type"] == "iptables"
        assert result["default_policy"]["INPUT"] == "DROP"
        assert result["default_policy"]["FORWARD"] == "DROP"
        assert result["default_policy"]["OUTPUT"] == "ACCEPT"
        assert len(result["rules"]) > 0
        assert "ssh" in str(result["open_ports"]) or "22" in str(result["open_ports"]) or "http" in str(result["open_ports"])
        assert len(result["logging"]) > 0

    def test_parse_iptables_save(self, analyzer: SecurityAnalyzer) -> None:
        """iptables-save format is parsed correctly."""
        result = analyzer.analyze_firewall_config(IPTABLES_SAVE_CONFIG, "iptables")

        assert result["firewall_type"] == "iptables"
        assert "22" in str(result["open_ports"])
        assert "80" in str(result["open_ports"])
        # Anti-spoofing rule
        spoof_rules = [r for r in result["rules"] if "10.0.0.0" in str(r.get("source", ""))]
        assert len(spoof_rules) > 0
        # Logging
        assert len(result["logging"]) > 0

    def test_parse_ufw_status(self, analyzer: SecurityAnalyzer) -> None:
        """ufw status output is parsed correctly."""
        result = analyzer.analyze_firewall_config(UFW_CONFIG, "ufw")

        assert result["firewall_type"] == "ufw"
        assert result["default_policy"]["INPUT"] == "deny"
        assert result["default_policy"]["OUTPUT"] == "allow"
        assert "22" in str(result["open_ports"])
        assert "80" in str(result["open_ports"])
        assert "443" in str(result["open_ports"])
        assert len(result["logging"]) > 0

    def test_parse_nftables(self, analyzer: SecurityAnalyzer) -> None:
        """nftables config is parsed correctly."""
        result = analyzer.analyze_firewall_config(NFTABLES_CONFIG, "nftables")

        assert result["firewall_type"] == "nftables"
        assert result["default_policy"]["input"] == "drop"
        assert result["default_policy"]["output"] == "drop"
        assert len(result["rules"]) > 0
        assert len(result["logging"]) > 0

    def test_parse_pf(self, analyzer: SecurityAnalyzer) -> None:
        """pf.conf is parsed correctly."""
        result = analyzer.analyze_firewall_config(PF_CONFIG, "pf")

        assert result["firewall_type"] == "pf"
        assert "22" in str(result["open_ports"])
        assert "80" in str(result["open_ports"])
        assert len(result["rules"]) > 0

    def test_autodetect_iptables(self, analyzer: SecurityAnalyzer) -> None:
        """Auto-detects iptables from -L output."""
        result = analyzer.analyze_firewall_config(IPTABLES_CONFIG, "auto")
        assert result["firewall_type"] == "iptables"

    def test_autodetect_ufw(self, analyzer: SecurityAnalyzer) -> None:
        """Auto-detects ufw from status output."""
        result = analyzer.analyze_firewall_config(UFW_CONFIG, "auto")
        assert result["firewall_type"] == "ufw"

    def test_autodetect_nftables(self, analyzer: SecurityAnalyzer) -> None:
        """Auto-detects nftables from config."""
        result = analyzer.analyze_firewall_config(NFTABLES_CONFIG, "auto")
